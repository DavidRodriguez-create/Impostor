/**
 * Select a random word from the given theme keys
 * @param {string[]} themeKeys - Array of theme keys to select from
 * @returns {object} Object with themeKey, wordKey, and word text
 */
export function selectRandomWord(themeKeys) {
  const lang = localStorage.getItem('language') || 'es';
  const themes = WORD_THEMES[lang] || WORD_THEMES.es;
  
  if (!themeKeys || themeKeys.length === 0) {
    return { 
      themeKey: null, 
      wordKey: null, 
      word: lang === 'en' ? "Word" : "Palabra" 
    };
  }

  // Combine all words from selected themes with their keys
  const allWords = [];
  themeKeys.forEach(themeKey => {
    if (themes[themeKey]) {
      Object.keys(themes[themeKey].words).forEach(wordKey => {
        allWords.push({ 
          themeKey, 
          wordKey, 
          word: themes[themeKey].words[wordKey] 
        });
      });
    }
  });

  if (allWords.length === 0) {
    return { 
      themeKey: null, 
      wordKey: null, 
      word: lang === 'en' ? "Word" : "Palabra" 
    };
  }

  // Pick random word
  return allWords[Math.floor(Math.random() * allWords.length)];
}

/**
 * Get translated word by theme key and word key
 * @param {string} themeKey - Theme key
 * @param {string} wordKey - Word key in theme
 * @returns {string} Translated word
 */
export function getTranslatedWord(themeKey, wordKey) {
  const lang = localStorage.getItem('language') || 'es';
  const themes = WORD_THEMES[lang] || WORD_THEMES.es;
  
  if (!themeKey || !wordKey || !themes[themeKey]) {
    return lang === 'en' ? "Word" : "Palabra";
  }
  
  return themes[themeKey].words[wordKey] || (lang === 'en' ? "Word" : "Palabra");
}

/**
 * Get all available theme keys
 * @returns {string[]} Array of theme keys
 */
export function getThemeKeys() {
  const lang = localStorage.getItem('language') || 'es';
  const themes = WORD_THEMES[lang] || WORD_THEMES.es;
  return Object.keys(themes);
}

/**
 * Get theme data by key
 * @param {string} themeKey - Theme key
 * @returns {object|null} Theme data or null if not found
 */
export function getTheme(themeKey) {
  const lang = localStorage.getItem('language') || 'es';
  const themes = WORD_THEMES[lang] || WORD_THEMES.es;
  return themes[themeKey] || null;
}

/**
 * Get all themes for current language
 * @returns {object} All themes
 */
export function getAllThemes() {
  const lang = localStorage.getItem('language') || 'es';
  return WORD_THEMES[lang] || WORD_THEMES.es;
}
